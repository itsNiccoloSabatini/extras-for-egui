//! # extras-for-egui
//!
//! A collection of extra widgets and utilities for [egui](https://github.com/emilk/egui).
//!
//! This library provides additional widgets and helpers that extend the functionality
//! of egui, making it easier to build rich graphical user interfaces.
//!
//! ## Usage
//!
//! Add this to your `Cargo.toml`:
//!
//! ```toml
//! [dependencies]
//! extras-for-egui = "0.1.0"
//! egui = "0.33.0"
//! ```
//!
//! ## Examples
//!
//! See the `examples/` directory for usage examples.

